package projectfile;

public class PrimitiveType {

	public static void main(String[] args) {
		boolean tf = true;
		char ch = 'A';
		byte bint = 1;
		short sint = 2;
		int a = 3;
		long b = 4L;
		float c = 5f;
		double d = 6;
		//값을 담는것들
		
		String e= "202006005 김남륜";
		//주소를 담는것
		
		System.out.println("boolean tf =" +tf);
		System.out.println("char ch = " + ch);
		System.out.println("byte bint =" + bint);
		System.out.println("short sint =" + sint);
		System.out.println("int a =" + a);
		System.out.println("long b =" + b);
		System.out.println("float c =" + c);
		System.out.println("double d =" + d);
		System.out.println("string e =" +e);
		
	}

}
